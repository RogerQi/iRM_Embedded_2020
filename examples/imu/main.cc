#include "main.h"
#include "spi.h"

void RM_RTOS_Init(void) {
  uint8_t tx = 0x75 | 0x80;
  uint8_t rx;
  HAL_SPI_Transmit(&hspi5, &tx, 1, 55);
  HAL_SPI_Receive(&hspi5, &rx, 1, 55);
}
